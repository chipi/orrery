/**
 * Fetches external assets at build time per ADR-016.
 *
 * Slice 1 scope: Google Fonts (Bebas Neue, Space Mono, Crimson Pro).
 * Future scopes (Slice 3+): planet textures, agency logos, NASA mission imagery.
 *
 * The script:
 *  1. Requests CSS from Google Fonts API for each family.
 *  2. Extracts woff2 URLs from the CSS.
 *  3. Downloads each woff2 to public/fonts/.
 *  4. Rewrites the CSS to point to local /fonts/ paths.
 *  5. Writes a combined src/lib/styles/fonts.css.
 *
 * Run via: npm run fetch-assets
 */

import { writeFile, mkdir } from 'node:fs/promises';
import { join, dirname } from 'node:path';

interface FontDef {
  family: string;
  cssParam: string;
}

const FONTS: FontDef[] = [
  { family: 'Bebas Neue', cssParam: 'Bebas+Neue' },
  { family: 'Space Mono', cssParam: 'Space+Mono:wght@400;700' },
  { family: 'Crimson Pro', cssParam: 'Crimson+Pro:ital,wght@0,400;0,600;1,400' },
];

const BASE_URL = 'https://fonts.googleapis.com/css2';
// User-Agent that triggers woff2 responses from Google Fonts.
const UA =
  'Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0 Safari/537.36';

const FONTS_DIR = 'public/fonts';
const CSS_OUT = 'src/lib/styles/fonts.css';

async function fetchFontCss(font: FontDef): Promise<string> {
  const url = `${BASE_URL}?family=${font.cssParam}&display=swap`;
  const res = await fetch(url, { headers: { 'User-Agent': UA } });
  if (!res.ok) throw new Error(`Failed to fetch CSS for ${font.family}: HTTP ${res.status}`);
  return res.text();
}

async function downloadFile(url: string, dest: string): Promise<void> {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`Failed to download ${url}: HTTP ${res.status}`);
  const buffer = Buffer.from(await res.arrayBuffer());
  await writeFile(dest, buffer);
}

function extractWoff2Urls(css: string): Set<string> {
  const urls = new Set<string>();
  const regex = /url\((https:\/\/[^)]+\.woff2)\)/g;
  let match;
  while ((match = regex.exec(css)) !== null) {
    urls.add(match[1]);
  }
  return urls;
}

async function main() {
  await mkdir(FONTS_DIR, { recursive: true });
  await mkdir(dirname(CSS_OUT), { recursive: true });

  let combinedCss =
    '/* Auto-generated by scripts/fetch-assets.ts. Do not edit by hand.\n' +
    '   Re-run with: npm run fetch-assets */\n\n';
  let totalDownloaded = 0;

  for (const font of FONTS) {
    console.log(`Fetching ${font.family}...`);
    const css = await fetchFontCss(font);
    const urls = extractWoff2Urls(css);

    let localCss = css;
    for (const url of urls) {
      const filename = url.split('/').pop();
      if (!filename) throw new Error(`Could not derive filename from ${url}`);
      const localPath = join(FONTS_DIR, filename);
      await downloadFile(url, localPath);
      localCss = localCss.replaceAll(url, `/fonts/${filename}`);
      totalDownloaded++;
    }

    combinedCss += `/* ${font.family} */\n${localCss}\n`;
  }

  await writeFile(CSS_OUT, combinedCss);
  console.log(`Done. Downloaded ${totalDownloaded} woff2 files; wrote ${CSS_OUT}`);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
