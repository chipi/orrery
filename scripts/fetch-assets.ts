/**
 * Generic gallery fetcher. For each entry: copy from missions if
 * configured, otherwise fetch from NASA Images API, finally fall back
 * to the curated Wikimedia filename if both fail. Writes the count
 * manifest so the UI can render thumbnails.
 */
async function fetchPanelGallery(
  label: string,
  queries: GalleryQuery[],
  outputDir: string,
  manifestPath: string,
  manifestShape: 'count-map' | 'singleton',
): Promise<number> {
  await mkdir(outputDir, { recursive: true });
  const manifest: Record<string, number> = {};
  let totalPhotos = 0;

  for (let i = 0; i < queries.length; i++) {
    const q = queries[i];
    const entityDir = manifestShape === 'singleton' ? outputDir : join(outputDir, q.id);
    await mkdir(entityDir, { recursive: true });
    process.stdout.write(`  ${q.id}…`);

    // Path 1 — copy from missions/{id}/ when configured.
    if (q.copyFromMission) {
      const sourceDir = join(MISSIONS_DIR, q.copyFromMission);
      try {
        const files = (await readdir(sourceDir)).filter((f) => /^\d{2}\.jpg$/.test(f)).sort();
        let saved = 0;
        for (const file of files) {
          await copyFile(join(sourceDir, file), join(entityDir, file));
          saved++;
        }
        if (saved > 0) {
          manifest[q.id] = saved;
          totalPhotos += saved;
          process.stdout.write(` copied from missions (${saved})\n`);
          continue;
        }
        process.stdout.write(' no mission images, falling back to NASA…');
      } catch {
        process.stdout.write(' missions/ unavailable, falling back to NASA…');
      }
    }

    // Path 2 — optional Commons hero (01), then NASA search.
    let urls: string[] = [];
    let saved = 0;
    if (q.commonsHeroFirst) {
      try {
        const url = `${WIKIMEDIA_FILEPATH_BASE}/${encodeURIComponent(q.commonsHeroFirst)}?width=800`;
        await downloadFromWikimedia(url, join(entityDir, '01.jpg'));
        saved = 1;
        process.stdout.write(' commons-hero');
      } catch {
        /* NASA may still populate 01 */
      }
    }

    if (!q.skipNasa) {
      try {
        urls = await fetchNasaGalleryUrls(q.query, PANEL_GALLERY_MAX);
      } catch {
        // NASA returned nothing usable. Single-image cover via
        // wikimediaFallback only when slot 01 is still empty.
        if (saved === 0 && q.wikimediaFallback) {
          try {
            const url = `${WIKIMEDIA_FILEPATH_BASE}/${encodeURIComponent(q.wikimediaFallback)}?width=800`;
            await downloadFromWikimedia(url, join(entityDir, '01.jpg'));
            saved = 1;
            process.stdout.write(' wikimedia-cover');
          } catch {
            // Fall through to wikimediaGallery top-up below.
          }
        }
      }
    }

    // NASA fills the next free slots (never overwrites a commons hero).
    for (let n = 0; n < urls.length && saved < PANEL_GALLERY_MAX; n++) {
      const filename = `${String(saved + 1).padStart(2, '0')}.jpg`;
      try {
        const imgRes = await fetch(urls[n]);
        if (!imgRes.ok) continue;
        const buffer = Buffer.from(await imgRes.arrayBuffer());
        await writeFile(join(entityDir, filename), buffer);
        saved++;
      } catch {
        // Single-image fail; continue with the rest.
      }
    }

    // Top up from the curated Wikimedia gallery list when NASA + cover
    // fallback fell short of GALLERY_MIN_TARGET. Same shape as
    // topUpWikimediaGallery for missions: filenames tried in order,
    // missing files skipped silently.
    if (saved < GALLERY_MIN_TARGET && q.wikimediaGallery && q.wikimediaGallery.length > 0) {
      const before = saved;
      for (const fname of q.wikimediaGallery) {
        if (saved >= GALLERY_MIN_TARGET) break;
        const slot = `${String(saved + 1).padStart(2, '0')}.jpg`;
        try {
          const url = `${WIKIMEDIA_FILEPATH_BASE}/${encodeURIComponent(fname)}?width=800`;
          await downloadFromWikimedia(url, join(entityDir, slot));
          saved++;
        } catch {
          // Skip missing file; continue with the next candidate.
        }
        await new Promise((resolve) => setTimeout(resolve, WIKIMEDIA_DELAY_MS));
      }
      if (saved > before) process.stdout.write(` +${saved - before} wikimedia-gallery`);
    }

    if (saved === 0) {
      process.stdout.write(' ⚠ skipped (no images)');
    }

    manifest[q.id] = saved;
    totalPhotos += saved;
    process.stdout.write(` ${saved}\n`);

    if (i < queries.length - 1) {
      await new Promise((resolve) => setTimeout(resolve, NASA_API_DELAY_MS));
    }
  }

  // Write manifest. Singleton shape → `{count: N}`; count-map → sorted `{id: count}`.
  if (manifestShape === 'singleton') {
    const id = queries[0]?.id ?? 'sun';
    const count = manifest[id] ?? 0;
    await writeFile(manifestPath, JSON.stringify({ count }, null, 2) + '\n');
  } else {
    const sorted: Record<string, number> = {};
    for (const k of Object.keys(manifest).sort()) sorted[k] = manifest[k];
    await writeFile(manifestPath, JSON.stringify(sorted, null, 2) + '\n');
  }
  console.log(`  → ${label} manifest: ${manifestPath} (${totalPhotos} photos total)`);
  return totalPhotos;
}

// ──────────────────────────────────────────────────────────────────────
// MISSION TRAJECTORY THUMBNAILS — v0.1.11 / Theme A.A2
//
// 240×120 PNG previews rendered at build time using node-canvas.
// Mars-bound missions get a heliocentric view: Sun, Earth orbit,
// destination orbit, and the outbound arc (uses real V∞ when present
// per mission.flight.arrival.v_infinity_km_s — same code path as
// /explore overlay rings + /fly trajectory).
//
// Moon-bound missions get a cislunar view: Earth at canvas centre +
// Moon orbit ring + a curved arc to a Moon position derived from
// transit_days (matches the /fly Moon-mode geometry).
//
// PNGs land at static/images/missions/thumbnails/{id}.png. Per
// CLAUDE.md ADR-016 these are build-time assets — no runtime canvas
// → image work needed.
// ──────────────────────────────────────────────────────────────────────

const THUMBNAILS_DIR = 'static/images/missions/thumbnails';
const THUMBNAIL_W = 240;
const THUMBNAIL_H = 120;
const THUMBNAIL_PX_PER_AU = 11; // tuned so Saturn (9.5 AU) just fits in width

interface MissionThumbnailRecord {
  id: string;
  dest: Destination;
  color: string;
  departure_date: string;
  transit_days: number;
  type?: string;
  flight?: {
    arrival?: { v_infinity_km_s?: number };
    cislunar_profile?: CislunarProfile;
  };
}

// Per-phase colour for cislunar thumbnails (ADR-058). Phase-typed
// strokes let users distinguish profile families at a glance: parking
// + tli_coast vs spiral_earth vs descent.
const CISLUNAR_PHASE_COLORS: Record<string, string> = {
  parking: '#4b9cd3',
  tli_coast: '#ffd166',
  lunar_orbit: '#c77dff',
  lunar_flyby: '#ff9933',
  descent: '#ef476f',
  ascent: '#ef476f',
  tei_coast: '#06d6a0',
  reentry: '#ef476f',
  spiral_earth: '#4b9cd3',
  spiral_lunar: '#c77dff',
};

function paintHeliocentricThumbnail(
  ctx: import('canvas').CanvasRenderingContext2D,
  destinationId: DestinationId,
  depDay: number,
  arrivalVInf: number | undefined,
  missionColor: string,
): void {
  const cx = THUMBNAIL_W / 2;
  const cy = THUMBNAIL_H / 2;
  const destA = DESTINATIONS[destinationId].a;

  // Background
  ctx.fillStyle = '#04040c';
  ctx.fillRect(0, 0, THUMBNAIL_W, THUMBNAIL_H);

  // Faint orbit rings (Earth + destination). Auto-clamp scale so the
  // largest orbit fits with margin.
  const maxAu = Math.max(R_EARTH_AU, destA);
  const scale = Math.min(THUMBNAIL_PX_PER_AU, (Math.min(cx, cy) - 8) / maxAu);

  ctx.beginPath();
  ctx.arc(cx, cy, R_EARTH_AU * scale, 0, Math.PI * 2);
  ctx.strokeStyle = 'rgba(75,156,211,0.35)';
  ctx.lineWidth = 0.6;
  ctx.stroke();
  ctx.beginPath();
  ctx.arc(cx, cy, destA * scale, 0, Math.PI * 2);
  ctx.strokeStyle = missionColor;
  ctx.globalAlpha = 0.35;
  ctx.lineWidth = 0.6;
  ctx.stroke();
  ctx.globalAlpha = 1;

  // Sun
  ctx.fillStyle = '#fff8c0';
  ctx.beginPath();
  ctx.arc(cx, cy, 2.5, 0, Math.PI * 2);
  ctx.fill();

  // Outbound arc — same code path as /fly + /explore overlay
  const earthDep = earthPos(depDay);
  const arc = outboundArc(earthDep, 80, destA, arrivalVInf);

  ctx.beginPath();
  arc.forEach((p, i) => {
    const x = cx + p.x * scale;
    const y = cy + p.z * scale;
    if (i === 0) ctx.moveTo(x, y);
    else ctx.lineTo(x, y);
  });
  ctx.strokeStyle = missionColor;
  ctx.lineWidth = 1.4;
  ctx.shadowColor = missionColor;
  ctx.shadowBlur = 4;
  ctx.stroke();
  ctx.shadowBlur = 0;

  // Departure node (teal) + arrival node (gold)
  const dep = arc[0];
  const arr = arc[arc.length - 1];
  ctx.fillStyle = '#4ecdc4';
  ctx.beginPath();
  ctx.arc(cx + dep.x * scale, cy + dep.z * scale, 2.5, 0, Math.PI * 2);
  ctx.fill();
  ctx.fillStyle = '#ffc850';
  ctx.beginPath();
  ctx.arc(cx + arr.x * scale, cy + arr.z * scale, 2.5, 0, Math.PI * 2);
  ctx.fill();
}

function paintCislunarThumbnail(
  ctx: import('canvas').CanvasRenderingContext2D,
  mission: MissionThumbnailRecord,
): void {
  const cx = THUMBNAIL_W / 2;
  const cy = THUMBNAIL_H / 2;
  // Background
  ctx.fillStyle = '#04040c';
  ctx.fillRect(0, 0, THUMBNAIL_W, THUMBNAIL_H);

  // Build the cislunar trajectory in ECI km. Round-trip detection
  // mirrors applyMissionAsLoaded() in /fly/+page.svelte.
  const missionType = (mission.type ?? '').toUpperCase();
  const isReturnTrip =
    missionType.includes('SAMPLE RETURN') ||
    missionType.includes('CREWED') ||
    missionType.includes('FLYBY');
  const traj = buildCislunarTrajectory(mission.flight?.cislunar_profile, {
    dep_day_sim: 0,
    transit_days: mission.transit_days ?? 4,
    is_return_trip: isReturnTrip,
  });

  // Project x/z (top-down ecliptic plane) onto the 240×120 canvas.
  // Scale so A_MOON_KM (~384,400 km) is ~45 px from Earth — leaves
  // room for the full trajectory plus a margin.
  const scale = 45 / A_MOON_KM;
  const project = (p: { x: number; z: number }): [number, number] => [
    cx + p.x * scale,
    cy + p.z * scale,
  ];

  // Moon orbit ring (Tier 1 truth — circle around Earth at A_MOON_KM)
  ctx.beginPath();
  ctx.arc(cx, cy, A_MOON_KM * scale, 0, Math.PI * 2);
  ctx.strokeStyle = 'rgba(170,170,204,0.25)';
  ctx.lineWidth = 0.6;
  ctx.stroke();

  // Earth at origin.
  ctx.fillStyle = '#3a8fcc';
  ctx.beginPath();
  ctx.arc(cx, cy, Math.max(3, R_EARTH_KM * scale * 4), 0, Math.PI * 2);
  ctx.fill();

  // Moon at flyby_day position.
  if (traj.moon_track.length > 0) {
    const flybyIdx = Math.floor(traj.moon_track.length * (isReturnTrip ? 0.5 : 1));
    const moonAtFlyby = traj.moon_track[Math.min(flybyIdx, traj.moon_track.length - 1)];
    const [mx, my] = project(moonAtFlyby);
    ctx.fillStyle = '#cfcfcf';
    ctx.beginPath();
    ctx.arc(mx, my, 3.5, 0, Math.PI * 2);
    ctx.fill();
  }

  // Trajectory phases — each rendered in its phase-typed colour.
  for (const phase of traj.phases) {
    if (phase.points.length < 2) continue;
    ctx.beginPath();
    phase.points.forEach((p, i) => {
      const [x, y] = project(p);
      if (i === 0) ctx.moveTo(x, y);
      else ctx.lineTo(x, y);
    });
    ctx.strokeStyle = CISLUNAR_PHASE_COLORS[phase.type] ?? mission.color;
    ctx.lineWidth = 1.4;
    ctx.shadowColor = ctx.strokeStyle;
    ctx.shadowBlur = 3;
    ctx.stroke();
    ctx.shadowBlur = 0;
  }

  // Departure node (teal) on the parking orbit / first phase start.
  if (traj.phases.length > 0 && traj.phases[0].points.length > 0) {
    const [dx, dy] = project(traj.phases[0].points[0]);
    ctx.fillStyle = '#4ecdc4';
    ctx.beginPath();
    ctx.arc(dx, dy, 2.5, 0, Math.PI * 2);
    ctx.fill();
  }
  // Arrival node (gold) on the last phase end.
  const lastPhase = traj.phases[traj.phases.length - 1];
  if (lastPhase && lastPhase.points.length > 0) {
    const [ax, ay] = project(lastPhase.points[lastPhase.points.length - 1]);
    ctx.fillStyle = '#ffc850';
    ctx.beginPath();
    ctx.arc(ax, ay, 2.5, 0, Math.PI * 2);
    ctx.fill();
  }
}

async function fetchMissionThumbnails(): Promise<number> {
  await mkdir(THUMBNAILS_DIR, { recursive: true });
  let saved = 0;

  const rootEntries = await readdir('static/data/missions', { withFileTypes: true });
  const destDirs = rootEntries.filter((e) => e.isDirectory()).map((e) => e.name);

  for (const dest of destDirs) {
    const dir = `static/data/missions/${dest}`;
    const files = (await readdir(dir)).filter((f) => f.endsWith('.json'));
    for (const file of files) {
      const raw = await readFile(join(dir, file), 'utf8');
      const m = JSON.parse(raw) as MissionThumbnailRecord;
      const canvas = createCanvas(THUMBNAIL_W, THUMBNAIL_H);
      const ctx = canvas.getContext('2d');
      const depDay = dateToSimDay(m.departure_date) ?? 0;
      const color = m.color || '#4ecdc4';

      if (m.dest === 'MOON') {
        paintCislunarThumbnail(ctx, m);
      } else {
        const hid = missionDestToHeliocentricDestinationId(m.dest);
        if (!hid) {
          throw new Error(
            `Thumbnail: mission ${m.id} has dest ${m.dest} without heliocentric mapping`,
          );
        }
        const vInf = m.flight?.arrival?.v_infinity_km_s;
        paintHeliocentricThumbnail(ctx, hid, depDay, vInf, color);
      }

      const buffer = canvas.toBuffer('image/png');
      await writeFile(join(THUMBNAILS_DIR, `${m.id}.png`), buffer);
      saved++;
      process.stdout.write(`  ${m.id} `);
    }
  }

  process.stdout.write('\n');
  console.log(`  → ${saved} thumbnails written to ${THUMBNAILS_DIR}`);
  return saved;
}

